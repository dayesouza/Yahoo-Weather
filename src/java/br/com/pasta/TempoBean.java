package br.com.pasta;

/**
 *
 * @author dayennesouza
 */
public class TempoBean {

    private String resultado;
    private String local;
    private String unit;
    private String data;
    private String texto_situacao;
    private String temperatura;
    private String cod_situacao;
    private String query;
    private String cor_temp;

    public String getResultado() {
        return resultado;
    }

    public void setResultado(String resultado) {
        this.resultado = resultado;
    }

    public String getLocal() {
        return local;
    }

    public void setLocal(String local) {
        this.local = local;
    }

    public String getUnit() {
        return unit;
    }

    public void setUnit(String unit) {
        this.unit = unit;
    }

    public String getCod_situacao() {
        return cod_situacao;
    }

    public void setCod_situacao(String cod_situacao) {
        this.cod_situacao = cod_situacao;
    }

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }

    public String getTexto_situacao() {
        return texto_situacao;
    }

    public void setTexto_situacao(String texto_situacao) {
        this.texto_situacao = texto_situacao;
    }

    public String getTemperatura() {
        return temperatura;
    }

    public void setTemperatura(String temperatura) {
        this.temperatura = temperatura;
    }

    public String getQuery() {
        return query;
    }

    public void setQuery(String query) {
        this.query = query;
    }

    public String getCor_temp() {
        return cor_temp;
    }

    public void setCor_temp(String cor_temp) {
        this.cor_temp = cor_temp;
    }
    
    
    
}
